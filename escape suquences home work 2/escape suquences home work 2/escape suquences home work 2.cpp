#include <iostream>
using namespace std;

int main()
{
// this is back slash.
	cout << "dir sir\\madam,\n";
	cout << "how are you ?\n";
// this is double quote.
	cout << "my name is \"ilyass \", nice to meet you\n";

// this is tab
	cout << "ilyass    el    ksili\n";
	cout << "ilyass\tel\tksili\n";


	return 0;







}